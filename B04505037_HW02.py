#第一題

x = int(input("Please insert a number: "))
c = 0 
r = 10
k = 0
    
    
if(x>=0):
    y = x
else:
    y = -x
    
while(y>=1):        
    i = y%r
    if (i<=1):
        c += i*(2**k)
        k += 1
        y = y//r
    
if(x>0):       
    print (c) 
else:
    print(-c)
                     
    

